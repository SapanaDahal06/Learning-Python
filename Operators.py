#Arithmetic  Operators.
a = 10
b = 3
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Floor Division:", a // b)
print("Modulus:", a % b)
print("Exponent:", a ** b)

#Comparison (Relational) Operators
x = 10
y = 20
print(x == y)
print(x != y)
print(x < y)
print(x >= y)

#Logical Operators
a = 10
b = 20
print(a > 5 and b > 15)  # True
print(a > 15 or b > 15)  # True
print(not(a > 5))        # False

