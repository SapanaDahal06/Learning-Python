# List

fruits = ["apple", "banana", "mango"]
print(fruits[0])   # apple

# Tuple
colors = ("red", "green", "blue")
print(colors[1])   # green

# dictonary

student = {"name": "Sapana", "age": 21}
print(student["name"])   # Sapana
# Super simple rule to remember:

# List → [ ] changeable

# Tuple → ( ) unchangeable

# Dictionary → { } key:value

print(4 + 4)    # Addition → 8
print(4 - 4)    # Subtraction → 0
print(4 * 40)   # Multiplication → 160
print(4 / 40)   # Division → 0.1
